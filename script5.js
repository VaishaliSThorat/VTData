// Loops

// Print 0 to 4
for(let i=0 ; i<5 ; i++){
console.log(i)
}

// Print 0 to 3
for(let i=0 ; i<=3 ; i++){
    console.log(i)
}

// Print 5 times hello
for(let i=0 ; i<=5 ; i++){
    console.log('hello')
}

// Print table of 2
for(let i=2 ; i<=20 ; i=i+2){
    console.log(i)
}

// Print 5 to 0
for(let i=5 ; i>=0 ; i--){
    console.log(i)
}